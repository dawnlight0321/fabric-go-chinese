package peer

import (
	"encoding/json"

	"github.com/hyperledger/fabric/common/util"
)

type strArgs struct {
	Function string
	Args     []string
}

//unmashaljson将基于字符串的rest/json输入转换为
//基于[]字节的当前chaincodeinput结构。
func (c *ChaincodeInput) UnmarshalJSON(b []byte) error {
	sa := &strArgs{}
	err := json.Unmarshal(b, sa)
	if err != nil {
		return err
	}
	allArgs := sa.Args
	if sa.Function != "" {
		allArgs = append([]string{sa.Function}, sa.Args...)
	}
	c.Args = util.ToChaincodeArgs(allArgs...)
	return nil
}

